#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
	{
		return (0);
	}
	while ((i * i) != nb)
	{
		i++;
		if (i > 46342)
			return (0);
	}
	return (i);
}
